import * as openpgp from "openpgp";

const textEncoder = new TextEncoder();
const sessionStorageKey = "hushline:chat-private-jwk";

function assertClientCryptoSupport() {
  // OpenPGP.js v6 requires secure context + SubtleCrypto + Web Streams + BigInt.
  if (!window.isSecureContext) {
    throw new Error("Encryption requires a secure browser context (HTTPS).");
  }
  if (!window.crypto || !window.crypto.subtle) {
    throw new Error("Encryption requires Web Crypto support in this browser.");
  }
  if (typeof window.ReadableStream === "undefined") {
    throw new Error("Encryption requires Web Streams support in this browser.");
  }
  if (typeof BigInt === "undefined") {
    throw new Error("Encryption requires BigInt support in this browser.");
  }
  if (
    typeof openpgp === "undefined" ||
    typeof openpgp.readKey !== "function" ||
    typeof openpgp.createMessage !== "function" ||
    typeof openpgp.encrypt !== "function"
  ) {
    throw new Error("Encryption module failed to initialize.");
  }
}

function getDicewareWords() {
  const words = window.DICEWARE_WORDS;
  if (!Array.isArray(words) || words.length === 0) {
    throw new Error("Encryption padding dictionary is unavailable.");
  }
  return words;
}

function addPadding(value, blockSize = 512) {
  /**
   * To hide what field is being encrypted, we need to pad the value to a (roughly) fixed block size.
   * This function is used to create a padded version of the field by adding random words at the end until it
   * reaches a multiple of 512-character blocks.
   */

  value += "\n\n(Random text generated by Hush Line: lorum ipsum ";

  // Add padding words
  const dicewareWords = getDicewareWords();
  const targetLen = blockSize - (value.length % blockSize);
  let padding = "";
  while (padding.length < targetLen) {
    padding +=
      dicewareWords[Math.floor(Math.random() * dicewareWords.length)] + " ";
  }
  padding += ")";

  // Return the padded value
  return value + padding;
}

async function encryptMessage(publicKeyArmored, message) {
  const armoredKeys = Array.isArray(publicKeyArmored)
    ? publicKeyArmored.filter((key) => typeof key === "string" && key.trim())
    : [publicKeyArmored].filter((key) => typeof key === "string" && key.trim());
  if (armoredKeys.length === 0) {
    console.log(
      "Public keys not provided for encryption. Encryption cannot proceed.",
    );
    return false;
  }

  try {
    const publicKey = await Promise.all(
      armoredKeys.map((armoredKey) => openpgp.readKey({ armoredKey })),
    );
    const messageText = await openpgp.createMessage({ text: message });
    const encryptedMessage = await openpgp.encrypt({
      message: messageText,
      encryptionKeys: publicKey,
    });
    return encryptedMessage;
  } catch (error) {
    console.error("Error encrypting:", error);
    return false;
  }
}

function isArmoredMessage(value) {
  return (
    typeof value === "string" &&
    value.includes("-----BEGIN PGP MESSAGE-----") &&
    value.includes("-----END PGP MESSAGE-----")
  );
}

function bytesToBase64(bytes) {
  let binary = "";
  bytes.forEach((byte) => {
    binary += String.fromCharCode(byte);
  });
  return btoa(binary);
}

function canonicalStringify(value) {
  if (Array.isArray(value)) {
    return `[${value.map((item) => canonicalStringify(item)).join(",")}]`;
  }
  if (value && typeof value === "object") {
    return `{${Object.keys(value)
      .sort()
      .map((key) => `${JSON.stringify(key)}:${canonicalStringify(value[key])}`)
      .join(",")}}`;
  }
  return JSON.stringify(value);
}

function jsonFromScript(id, fallback) {
  const script = document.getElementById(id);
  if (!script?.textContent) {
    return fallback;
  }

  try {
    return JSON.parse(script.textContent);
  } catch (error) {
    return fallback;
  }
}

async function importChatPublicKey(publicKeyValue) {
  const publicJwk =
    typeof publicKeyValue === "string"
      ? JSON.parse(publicKeyValue)
      : publicKeyValue;
  return window.crypto.subtle.importKey(
    "jwk",
    publicJwk,
    {
      name: "ECDH",
      namedCurve: publicJwk.crv || "P-256",
    },
    false,
    [],
  );
}

async function encryptForChatPublicKey(
  plaintext,
  recipientChatKey,
  context,
  signingKey,
) {
  const recipientPublicKey = await importChatPublicKey(
    recipientChatKey.public_key || recipientChatKey,
  );
  const ephemeralKeyPair = await window.crypto.subtle.generateKey(
    {
      name: "ECDH",
      namedCurve: "P-256",
    },
    true,
    ["deriveKey"],
  );
  const messageKey = await window.crypto.subtle.deriveKey(
    {
      name: "ECDH",
      public: recipientPublicKey,
    },
    ephemeralKeyPair.privateKey,
    {
      name: "AES-GCM",
      length: 256,
    },
    false,
    ["encrypt"],
  );
  const iv = window.crypto.getRandomValues(new Uint8Array(12));
  const envelopeContext =
    context && signingKey
      ? {
          ...context,
          recipient_key_version: recipientChatKey.key_version || null,
          recipient_public_key_fingerprint:
            recipientChatKey.public_key_fingerprint || null,
        }
      : null;
  const encryptParams = {
    name: "AES-GCM",
    iv,
  };
  if (envelopeContext) {
    encryptParams.additionalData = textEncoder.encode(
      canonicalStringify(envelopeContext),
    );
  }
  const ciphertext = new Uint8Array(
    await window.crypto.subtle.encrypt(
      encryptParams,
      messageKey,
      textEncoder.encode(plaintext),
    ),
  );
  const ephemeralPublicJwk = await window.crypto.subtle.exportKey(
    "jwk",
    ephemeralKeyPair.publicKey,
  );

  const envelope = {
    algorithm: "ECDH-P256-AES-GCM",
    ephemeral_public_key: JSON.stringify(ephemeralPublicJwk),
    iv: bytesToBase64(iv),
    ciphertext: bytesToBase64(ciphertext),
  };
  if (envelopeContext) {
    envelope.v = 2;
    envelope.context = envelopeContext;
    envelope.signature = await signChatEnvelope(envelope, signingKey);
  }
  return JSON.stringify(envelope);
}

function getFieldValue(field) {
  if (
    field.tagName === "INPUT" ||
    field.tagName === "SELECT" ||
    field.tagName === "TEXTAREA"
  ) {
    return field.value;
  } else if (field.tagName === "UL") {
    const checkedValues = [];
    field
      .querySelectorAll(
        "input[type='checkbox']:checked, input[type='radio']:checked",
      )
      .forEach((input) => {
        checkedValues.push(input.value);
      });
    return checkedValues.join(", ");
  }
}

function getFieldLabel(field) {
  return field.getAttribute("data-label");
}

function getRecipientPublicKeys() {
  const recipientPublicKeysEl = document.getElementById("recipientPublicKeys");
  if (!recipientPublicKeysEl) {
    return [];
  }

  try {
    const recipientPublicKeys = JSON.parse(
      recipientPublicKeysEl.textContent || "[]",
    );
    if (!Array.isArray(recipientPublicKeys)) {
      return [];
    }
    return recipientPublicKeys.filter(
      (key) => typeof key === "string" && key.trim(),
    );
  } catch (error) {
    console.error("Error loading recipient public keys:", error);
    return [];
  }
}

function getRecipientPublicKeyEntries() {
  const recipientPublicKeyEntriesEl = document.getElementById(
    "recipientPublicKeyEntries",
  );
  if (!recipientPublicKeyEntriesEl) {
    return [];
  }

  try {
    const recipientPublicKeyEntries = JSON.parse(
      recipientPublicKeyEntriesEl.textContent || "[]",
    );
    if (!Array.isArray(recipientPublicKeyEntries)) {
      return [];
    }
    return recipientPublicKeyEntries.filter(
      (entry) =>
        entry &&
        Number.isInteger(entry.id) &&
        typeof entry.key === "string" &&
        entry.key.trim(),
    );
  } catch (error) {
    console.error("Error loading recipient public key entries:", error);
    return [];
  }
}

function getChatPublicKey(id) {
  const publicKey = jsonFromScript(id, null);
  return typeof publicKey === "string" && publicKey.trim() ? publicKey : null;
}

function getChatKeyDescriptor(id) {
  const descriptor = jsonFromScript(id, null);
  if (
    !descriptor ||
    typeof descriptor !== "object" ||
    typeof descriptor.public_key !== "string" ||
    !descriptor.public_key.trim()
  ) {
    return null;
  }
  return descriptor;
}

function normalizePrivateKeyBundle(value) {
  if (value?.ecdh_private_jwk) {
    return value;
  }
  return {
    ecdh_private_jwk: value,
    signing_private_jwk: null,
  };
}

function rememberedPrivateKeyBundleForChatKey(chatKey) {
  if (!chatKey) {
    return null;
  }
  try {
    const stored = JSON.parse(sessionStorage.getItem(sessionStorageKey) || "null");
    if (
      stored?.key_version !== chatKey.key_version ||
      stored.public_key !== chatKey.public_key ||
      (stored.public_signing_key || null) !==
        (chatKey.public_signing_key || null) ||
      !(stored.private_key_bundle || stored.private_jwk) ||
      !stored.expires_at ||
      Date.now() > Number(stored.expires_at)
    ) {
      return null;
    }
    return normalizePrivateKeyBundle(
      stored.private_key_bundle || stored.private_jwk,
    );
  } catch (error) {
    return null;
  }
}

async function importSigningPrivateKey(privateJwk) {
  if (!privateJwk) {
    return null;
  }
  const importJwk = {
    ...privateJwk,
    key_ops: ["sign"],
  };
  return window.crypto.subtle.importKey(
    "jwk",
    importJwk,
    {
      name: "ECDSA",
      namedCurve: importJwk.crv || "P-256",
    },
    false,
    ["sign"],
  );
}

async function initialConversationSigningKey(senderChatKey) {
  const restoredSigningKey =
    await window.HushLineChatKeys?.signingPrivateKeyForChatKey?.(
      senderChatKey,
    );
  if (restoredSigningKey) {
    return restoredSigningKey;
  }
  const privateKeyBundle = rememberedPrivateKeyBundleForChatKey(senderChatKey);
  if (!privateKeyBundle?.signing_private_jwk) {
    return null;
  }
  return importSigningPrivateKey(privateKeyBundle.signing_private_jwk);
}

async function signChatEnvelope(envelope, signingKey) {
  const signedPayload = {
    v: envelope.v,
    algorithm: envelope.algorithm,
    ephemeral_public_key: envelope.ephemeral_public_key,
    iv: envelope.iv,
    ciphertext: envelope.ciphertext,
    context: envelope.context,
  };
  const signature = new Uint8Array(
    await window.crypto.subtle.sign(
      {
        name: "ECDSA",
        hash: "SHA-256",
      },
      signingKey,
      textEncoder.encode(canonicalStringify(signedPayload)),
    ),
  );
  return bytesToBase64(signature);
}

function getMessageFields() {
  return Array.from(document.querySelectorAll(".form-field")).map((field) => ({
    name: field.name || field.querySelector("input")?.name,
    label: getFieldLabel(field),
    value: getFieldValue(field),
    encrypted: field.classList.contains("encrypted-field"),
  }));
}

function conversationBodyFromFields(messageFields) {
  return messageFields
    .map((field) => `${field.label}\n\n${field.value}\n\n==============`)
    .join("\n\n")
    .trim();
}

function replaceSubmittedFieldsWithConversationPlaceholder() {
  Array.from(document.querySelectorAll(".form-field")).forEach((field) => {
    const fieldName = field.name || field.querySelector("input")?.name;
    const fieldContainer = field.parentElement;
    if (!fieldName || !fieldContainer) {
      return;
    }

    fieldContainer.innerHTML = "";

    const hiddenInput = document.createElement("input");
    hiddenInput.type = "hidden";
    hiddenInput.name = fieldName;
    hiddenInput.value = "Stored in encrypted conversation.";

    const textarea = document.createElement("textarea");
    textarea.disabled = true;
    textarea.value = hiddenInput.value;

    fieldContainer.appendChild(hiddenInput);
    fieldContainer.appendChild(textarea);
  });
}

async function buildEncryptedConversationCopies(messageFields) {
  const recipientChatKey =
    getChatKeyDescriptor("recipientChatKey") ||
    (getChatPublicKey("recipientChatPublicKey")
      ? { public_key: getChatPublicKey("recipientChatPublicKey") }
      : null);
  const senderChatKey = getChatKeyDescriptor("senderChatKey");
  const signingKey = await initialConversationSigningKey(senderChatKey);
  const initialConversationNonce = document.querySelector(
    "input[name='owner_guard_nonce']",
  )?.value;
  if (
    !recipientChatKey?.public_key ||
    !recipientChatKey?.public_signing_key ||
    !senderChatKey?.public_key ||
    !senderChatKey?.public_signing_key ||
    !signingKey ||
    !initialConversationNonce
  ) {
    return {};
  }

  try {
    const conversationBody = conversationBodyFromFields(messageFields);
    const context = {
      purpose: "hushline.chat.initial_message",
      initial_conversation_nonce: initialConversationNonce,
      sender_key_version: senderChatKey.key_version || null,
      sender_public_key_fingerprint:
        senderChatKey.public_key_fingerprint || null,
      sender_public_signing_key_fingerprint:
        senderChatKey.public_signing_key_fingerprint || null,
    };
    const encryptedCopies = {
      recipient: await encryptForChatPublicKey(
        conversationBody,
        recipientChatKey,
        context,
        signingKey,
      ),
    };
    encryptedCopies.sender = await encryptForChatPublicKey(
      conversationBody,
      senderChatKey,
      context,
      signingKey,
    );
    return encryptedCopies;
  } catch (error) {
    console.error("Chat transcript encryption failed.");
    return {};
  }
}

async function submitEncryptedForm(form) {
  const response = await fetch(form.action, {
    method: form.method || "POST",
    body: new FormData(form),
    credentials: "same-origin",
    headers: {
      Accept: "text/html",
    },
  });
  const html = await response.text();
  if (response.url && response.url.startsWith(window.location.origin)) {
    window.history.replaceState({}, "", response.url);
  }
  document.open();
  document.write(html);
  document.close();
}

document.addEventListener("DOMContentLoaded", function () {
  const form = document.getElementById("messageForm");
  if (!form) {
    return;
  }
  const recipientPublicKeys = getRecipientPublicKeys();
  const recipientPublicKeyEntries = getRecipientPublicKeyEntries();

  form.addEventListener("submit", async function (event) {
    event.preventDefault();
    if (form.dataset.encryptionInProgress === "true") {
      return;
    }

    form.dataset.encryptionInProgress = "true";
    const submitButtons = form.querySelectorAll(
      "button[type='submit'], input[type='submit']",
    );
    submitButtons.forEach((button) => {
      button.disabled = true;
    });

    try {
      assertClientCryptoSupport();

      const messageFields = getMessageFields();
      messageFields.forEach((field) => {
        if (isArmoredMessage(field.value)) {
          throw new Error("Message appears already encrypted.");
        }
      });

      const encryptedConversationCopiesEl = document.getElementById(
        "encrypted_conversation_copies",
      );
      const encryptedConversationCopies = await buildEncryptedConversationCopies(
        messageFields,
      );
      if (encryptedConversationCopiesEl) {
        encryptedConversationCopiesEl.value = JSON.stringify(
          encryptedConversationCopies,
        );
      }

      if (recipientPublicKeys.length === 0) {
        if (!encryptedConversationCopies.recipient) {
          throw new Error("Client-side chat encryption failed.");
        }
        replaceSubmittedFieldsWithConversationPlaceholder();
        await submitEncryptedForm(form);
        return;
      }

      // Build an email body with all fields, encrypt it, and add it to the DOM as a hidden field
      let emailBody = "";
      messageFields.forEach((field) => {
        emailBody += `# ${field.label}\n\n${field.value}\n\n====================\n\n`;
      });
      const encryptedEmailBody = await encryptMessage(
        recipientPublicKeys,
        emailBody,
      );
      if (encryptedEmailBody) {
        const encryptedEmailBodyEl = document.getElementById(
          "encrypted_email_body",
        );
        if (!encryptedEmailBodyEl) {
          throw new Error("Encrypted email body field is missing.");
        }
        encryptedEmailBodyEl.value = encryptedEmailBody;
      } else {
        throw new Error("Client-side encryption failed for email body");
      }

      const encryptedEmailFieldsByRecipient = {};
      await Promise.all(
        recipientPublicKeyEntries.map(async (recipient) => {
          const encryptedFields = {};
          await Promise.all(
            messageFields
              .filter((field) => field.encrypted)
              .map(async (field) => {
                const encryptedValue = await encryptMessage(
                  recipient.key,
                  addPadding(field.value),
                );
                if (!encryptedValue) {
                  throw new Error(
                    `Client-side recipient encryption failed for field: ${field.name || "unknown"}`,
                  );
                }
                encryptedFields[field.name] = encryptedValue;
              }),
          );
          encryptedEmailFieldsByRecipient[recipient.id] = encryptedFields;
        }),
      );
      const encryptedEmailFieldsEl = document.getElementById(
        "encrypted_email_fields_by_recipient",
      );
      if (encryptedEmailFieldsEl) {
        encryptedEmailFieldsEl.value = JSON.stringify(
          encryptedEmailFieldsByRecipient,
        );
      }

      // Loop through all encrypted fields and encrypt them
      const encryptionPromises = Array.from(
        document.querySelectorAll(".encrypted-field"),
      ).map(async (field) => {
        const value = getFieldValue(field);
        if (isArmoredMessage(value)) {
          throw new Error("Message appears already encrypted.");
        }

        const paddedValue = addPadding(value);
        const encryptedValue = await encryptMessage(
          recipientPublicKeys,
          paddedValue,
        );
        if (encryptedValue) {
          // Replace the field with a hidden field and a disabled textarea (for show) containing the encrypted value
          let fieldName;
          if (field.tagName === "UL") {
            fieldName = field.querySelector("input").name;
          } else {
            fieldName = field.name;
          }

          // Empty out the field
          const fieldContainer = field.parentElement;
          fieldContainer.innerHTML = "";

          // Add a textarea with the encrypted value
          const hiddenInput = document.createElement("input");
          hiddenInput.type = "hidden";
          hiddenInput.name = fieldName;
          hiddenInput.value = encryptedValue;

          const textarea = document.createElement("textarea");
          textarea.disabled = true;
          textarea.value = encryptedValue;

          fieldContainer.appendChild(hiddenInput);
          fieldContainer.appendChild(textarea);
        } else {
          throw new Error(
            `Client-side encryption failed for field: ${field.name || "unknown"}`,
          );
        }
      });

      // Wait for all encryption operations to complete before submitting
      await Promise.all(encryptionPromises);
      await submitEncryptedForm(form);
    } catch (error) {
      console.error("Encryption error:", error);
      alert(
        "Message encryption failed. Your message was NOT submitted for security reasons. Please try again.",
      );
      form.dataset.encryptionInProgress = "false";
      submitButtons.forEach((button) => {
        button.disabled = false;
      });
    }
  });
});
