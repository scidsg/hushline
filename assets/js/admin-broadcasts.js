import * as openpgp from "openpgp";

function assertClientCryptoSupport() {
  if (!window.isSecureContext) {
    throw new Error("Encryption requires a secure browser context.");
  }
  if (!window.crypto || !window.crypto.subtle) {
    throw new Error("Encryption requires Web Crypto support.");
  }
  if (typeof window.ReadableStream === "undefined") {
    throw new Error("Encryption requires Web Streams support.");
  }
  if (typeof BigInt === "undefined") {
    throw new Error("Encryption requires BigInt support.");
  }
}

function dicewareWords() {
  if (
    !Array.isArray(window.DICEWARE_WORDS) ||
    window.DICEWARE_WORDS.length === 0
  ) {
    throw new Error("Encryption padding dictionary is unavailable.");
  }
  return window.DICEWARE_WORDS;
}

function addPadding(value, blockSize = 512) {
  value += "\n\n(Random text generated by Hush Line: lorum ipsum ";
  const words = dicewareWords();
  const targetLen = blockSize - (value.length % blockSize);
  let padding = "";
  while (padding.length < targetLen) {
    padding += words[Math.floor(Math.random() * words.length)] + " ";
  }
  return `${value}${padding})`;
}

function recipients() {
  const script = document.getElementById("broadcastEncryptionRecipients");
  if (!script?.textContent) {
    return [];
  }
  try {
    const parsed = JSON.parse(script.textContent);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

async function encryptMessage(publicKeys, message) {
  const keys = Array.isArray(publicKeys) ? publicKeys : [publicKeys];
  const parsedKeys = await Promise.all(
    keys
      .filter((key) => typeof key === "string" && key.trim())
      .map(async (armoredKey) => {
        try {
          return await openpgp.readKey({ armoredKey });
        } catch (error) {
          console.error("Skipping unusable recipient encryption key.", error);
          return null;
        }
      }),
  );
  const encryptionKeys = parsedKeys.filter(Boolean);
  if (encryptionKeys.length === 0) {
    throw new Error("Recipient encryption keys are missing.");
  }
  const openpgpMessage = await openpgp.createMessage({
    text: addPadding(message),
  });
  return openpgp.encrypt({
    message: openpgpMessage,
    encryptionKeys,
  });
}

function sendButtonWasUsed(event) {
  const submitter = event.submitter || document.activeElement;
  return submitter?.getAttribute("name") === "send_broadcast";
}

function setButtonsDisabled(form, disabled) {
  form
    .querySelectorAll("button[type='submit'], input[type='submit']")
    .forEach((button) => {
      button.disabled = disabled;
    });
}

document.addEventListener("DOMContentLoaded", function () {
  const form = document.querySelector("form[data-admin-broadcast-form='true']");
  const plaintext = document.getElementById("broadcast_plaintext");
  if (!form || !plaintext) {
    return;
  }

  form.addEventListener("submit", async function (event) {
    if (!sendButtonWasUsed(event)) {
      return;
    }
    event.preventDefault();
    if (form.dataset.encryptionInProgress === "true") {
      return;
    }

    const payloadTargetId = plaintext.dataset.encryptedPayloadTarget;
    const payloadTarget = payloadTargetId
      ? document.getElementById(payloadTargetId)
      : null;
    const failureTarget = form.querySelector(
      "input[name='encryption_failures']",
    );
    const recipientEntries = recipients();
    if (!payloadTarget || !failureTarget || recipientEntries.length === 0) {
      alert("No users with PGP message keys match this audience.");
      return;
    }

    form.dataset.encryptionInProgress = "true";
    setButtonsDisabled(form, true);

    try {
      assertClientCryptoSupport();
      const body = plaintext.value.trim();
      if (body.length < 10) {
        throw new Error("Message must be at least 10 characters.");
      }

      const encryptedPayloads = {};
      const encryptionFailures = [];
      await Promise.all(
        recipientEntries.map(async (recipient) => {
          try {
            encryptedPayloads[recipient.user_id] = await encryptMessage(
              recipient.public_keys,
              body,
            );
          } catch (error) {
            encryptionFailures.push(recipient.user_id);
            console.error(
              `Broadcast encryption failed for user ${recipient.user_id}.`,
              error,
            );
          }
        }),
      );
      if (Object.keys(encryptedPayloads).length === 0) {
        throw new Error("No recipient messages could be encrypted.");
      }
      payloadTarget.value = JSON.stringify(encryptedPayloads);
      failureTarget.value = JSON.stringify(encryptionFailures);
      plaintext.value = "";

      const submitIntent = document.createElement("input");
      submitIntent.type = "hidden";
      submitIntent.name = "send_broadcast";
      submitIntent.value = "Submit Messages";
      form.appendChild(submitIntent);
      form.submit();
    } catch (error) {
      console.error("Broadcast encryption failed.", error);
      alert(
        `Message encryption failed: ${error.message} No messages were submitted.`,
      );
      payloadTarget.value = "";
      failureTarget.value = "";
      form.dataset.encryptionInProgress = "false";
      setButtonsDisabled(form, false);
    }
  });
});
